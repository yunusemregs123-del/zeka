import re

def insert_new_translations():
    with open("src/lib/Translations.ts", "r") as f:
        content = f.read()
    
    # Simple strategy: for each language object, insert keys
    for lang, trans_dict in base_translations.items():
        lang_pattern = re.compile(rf"({lang}:\s*{{)(.*?)(}})", re.DOTALL)
        match = lang_pattern.search(content)
        if match:
             inner_content = match.group(2)
             formatted_json = ",\n".join([f'    {k}: {json.dumps(v)}' for k, v in trans_dict.items()])
             content = content.replace(match.group(0), f"{lang}: {{\n{formatted_json},\n{inner_content}}}")
             
    with open("src/lib/Translations.ts", "w") as f:
        f.write(content)

insert_new_translations()
